public class literals {
    public static void main(String[] args) {
        //literals 
        int num1 = 0b1011; // int literals  // binary
        int num2 = 0x7e; // hexadecimal
        int num3 = 012;  // octal


        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
    }
}
